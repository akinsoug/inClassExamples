//conditional practice

// a basic if statement, it runs the code inside the
// {} when the (condition) is true.
let iAmTrue = true;
if (iAmTrue) {
        console.log("True Test")
}

// a layered if statement
if (false) {
        console.log("This will never run.")
} else { 
        // we run what is in the  "else" block because the main
        // 'if' statement condition evaluated to false.
        console.log("This will always run")
}
